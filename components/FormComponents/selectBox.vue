<template>
  <div :class="$attrs.class">
    <ValidationProvider :name="$attrs.label" :rules="rules">
      <v-select
        v-model="innerValue"
        slot-scope="{ errors, valid }"
        :valid="valid"
        :error-messages="errors"
        v-bind="$attrs"
        v-on="$listeners"
      ></v-select>
    </ValidationProvider>
  </div>
</template>

<script>
import { ValidationProvider } from 'vee-validate'

export default {
  components: {
    ValidationProvider
  },
  props: {
    rules: {
      type: [Object, String],
      default: null
    },
    class: {
      type: String,
      default: ''
    },
    value: {
      type: null,
      default: null
    }
  },
  data: () => ({
    innerValue: null
  }),
  watch: {
    // Handles internal model changes.
    innerValue(newVal) {
      this.$emit('input', newVal)
    },
    // Handles external model changes.
    value(newVal) {
      this.innerValue = newVal
    }
  },
  created() {
    if (this.value) {
      this.innerValue = this.value
    }
  }
}
</script>
